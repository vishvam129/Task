# Find and Replace:

# Accepts a string, a word to find, and a word to replace it with.

String=input("Enter a String:- ")

find_word=input("Enter A Word :- ")

replace_word=input("Enter A word :- ")

# Replaces all occurrences of the word and displays the modified string.

print("final String :- ",String.replace(find_word,replace_word))